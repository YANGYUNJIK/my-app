app.post('/items', upload.single('image'), async (req, res) => {
  const db = getDb();
  const { name, type, imageBase64, stock } = req.body;

  if (!name || !type) return res.status(400).json({ error: 'ì´ë¦„ê³¼ ì¢…ë¥˜ëŠ” í•„ìˆ˜ì…ë‹ˆë‹¤.' });

  let imageUrl;
  try {
    if (req.file) {
      imageUrl = `${req.protocol}://${req.get('host')}/uploads/${req.file.filename}`;
    } else if (imageBase64) {
      const filename = `${Date.now()}.jpg`;
      const filepath = path.join(uploadDir, filename);
      fs.writeFileSync(filepath, Buffer.from(imageBase64, 'base64'));
      imageUrl = `${req.protocol}://${req.get('host')}/uploads/${filename}`;
    } else {
      imageUrl = `${req.protocol}://${req.get('host')}/uploads/logo.png`;
    }

    const newItem = {
      name,
      type,
      image: imageUrl,
      stock: stock === 'false' || stock === false ? false : true // ê¸°ë³¸ê°’ true
    };

    const result = await db.collection('items').insertOne(newItem);
    res.json({ success: true, item: { ...newItem, _id: result.insertedId } });
  } catch (err) {
    console.error('âŒ í•­ëª© ë“±ë¡ ì‹¤íŒ¨:', err);
    res.status(500).json({ error: 'ì„œë²„ ì˜¤ë¥˜' });
  }
});

